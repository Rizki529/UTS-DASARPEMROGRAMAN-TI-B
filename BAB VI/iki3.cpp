#include <iostream>
using namespace std;

int main()
{
    //Pengulangan Menaik
    cout << " Pengulangan Menaik " <<endl;
    for (int C = 0; C<10; C++)
    {
        cout << C++ << endl;
    }
    cout << "\n";
    // Pengulangan Menurun
    cout << " Pengulangan Menurun " <<endl;
    for (int D = 0; D > 10; D--)
    {
        cout << D << endl;
    }
    return 0;
}
